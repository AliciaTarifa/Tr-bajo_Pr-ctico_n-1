public class Ejercicio_10 {

	/*
	 * Muestre en un ejemplo cómo se usan los operadores incremento y decremento.
	 */
	public static void main(String[] args) {
		
		int numero = 6;
		int numero2 = 9;
		
		int incremento = ++numero;
		int decremento = --numero2;
		
		System.out.println("El operador de incremento da por resultado " + incremento);
		System.out.println("El operador de decremento da por resultado " + decremento);

	}

}
